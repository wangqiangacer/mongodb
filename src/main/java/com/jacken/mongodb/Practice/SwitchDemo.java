package com.jacken.mongodb.Practice;

public class SwitchDemo {
    public static void main(String[] args) {
        String day="3";
        switch (day){
            case "1":
                System.out.println("1111111111");
                break;
            case "2":
                System.out.println("2222222222");
                break;
            case "3":
                System.out.println("3333333333");
                break;
        }

    }
}
